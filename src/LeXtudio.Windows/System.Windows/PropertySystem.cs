public readonly struct ValueSource
{
    public BaseValueSource BaseValueSource { get; init; }
    public bool IsExpression { get; init; }
}

// Mirrors the member order/values of WPF System.Windows.BaseValueSource so the
// precedence comparisons in linked DataGrid code (e.g. GetCoercedTransferProperty-
// Value) are meaningful. The Uno bridge only ever reports Default or Local — it
// cannot observe styles/inheritance — but the full set keeps the type faithful.
public enum BaseValueSource
{
    Unknown = 0,
    Default = 1,
    Inherited = 2,
    DefaultStyle = 3,
    DefaultStyleTrigger = 4,
    Style = 5,
    TemplateTrigger = 6,
    StyleTrigger = 7,
    ImplicitStyleReference = 8,
    ParentTemplate = 9,
    ParentTemplateTrigger = 10,
    Local = 11,
}

public static class DependencyPropertyHelper
{
    // Distinguish a locally-set value from the default, the only precedence
    // levels the Uno bridge can observe (via ReadLocalValue). Styles and
    // inheritance are reported as Default.
    public static ValueSource GetValueSource(DependencyObject obj, Microsoft.UI.Xaml.DependencyProperty dp)
        => new()
        {
            BaseValueSource = obj.ReadLocalValue(dp) == Microsoft.UI.Xaml.DependencyProperty.UnsetValue
                ? BaseValueSource.Default
                : BaseValueSource.Local,
            IsExpression = false,
        };
}

public static class DependencyObjectExtensions
{
    // WPF DependencyObject.SetCurrentValueInternal — sets value without overriding a locally-set
    // value in WPF's property system. On HAS_UNO there is no such distinction; plain SetValue suffices.
    public static void SetCurrentValueInternal(this DependencyObject d, DependencyProperty dp, object? value)
        => d.SetValue(dp, value);

    // WPF FrameworkElement.HasNonDefaultValue — returns true when the property has been explicitly set
    // (not just inherited or defaulted). On HAS_UNO we approximate: if the local value equals the
    // property's default we treat it as "default". This is conservative but safe for ToolBar usage.
    public static bool HasNonDefaultValue(this DependencyObject d, DependencyProperty dp)
    {
        object? local = d.ReadLocalValue(dp);
        return local != Microsoft.UI.Xaml.DependencyProperty.UnsetValue;
    }

}
